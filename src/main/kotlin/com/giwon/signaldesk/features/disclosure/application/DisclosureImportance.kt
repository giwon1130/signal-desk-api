package com.giwon.signaldesk.features.disclosure.application

/**
 * 공시 중요도 — 보유 종목 주가에 미치는 영향 기준.
 *  - HIGH:   주가에 직접·강하게 작용 (증자/CB·BW/자기주식/실적/M&A/최대주주변경/횡령·배임/상장폐지 등)
 *  - MEDIUM: 영향 가능하거나 분류 불명확 (기본값 — 알려지지 않은 공시는 숨기지 않고 MEDIUM 처리)
 *  - LOW:    루틴·노이즈 (IR개최/지분대량보유보고/임원소유상황/정기보고서/주총소집 등) → 푸시 제외
 */
enum class DisclosureImportance { HIGH, MEDIUM, LOW }

/**
 * OpenDART 공시 제목(reportNm)만으로 중요도를 추정하는 순수 함수 분류기.
 *
 * DART list.json 응답에는 항목별 pblntf_ty(공시유형 코드)가 없지만, 제목 자체에 유형이
 * 그대로 박혀 있다 (예: "주요사항보고서(자기주식취득결정)", "[기재정정]단일판매·공급계약체결").
 * 따라서 제목 키워드 = 공시유형 + 세부내용을 동시에 커버한다.
 *
 * 판정 순서: HIGH 키워드 → LOW 키워드 → 그 외 MEDIUM(기본).
 * LOW 보다 HIGH 를 먼저 보므로, 노이즈처럼 보이는 정정([기재정정] 등)이라도 핵심 사안이
 * 중요하면 HIGH 로 잡힌다.
 */
object DisclosureClassifier {

    /** 주가에 직접 작용하는 핵심 사안 — 공백·가운뎃점 제거 후 부분일치. */
    private val HIGH = listOf(
        // 자본 변동
        "유상증자", "무상증자", "유상감자", "감자결정", "주주배정", "제3자배정", "주식소각",
        // 메자닌
        "전환사채", "신주인수권부사채", "교환사채", "조건부자본증권",
        // 자기주식
        "자기주식취득", "자기주식처분", "자기주식소각", "자기주식취득신탁", "자기주식취득신탁계약해지",
        // 영업/계약
        "단일판매", "공급계약", "영업양수", "영업양도", "자산양수", "자산양도",
        "유형자산양수", "유형자산양도", "타법인주식", "출자증권",
        // 실적
        "잠정실적", "영업실적", "손익구조", "매출액또는",
        // 지배구조/구조개편
        "합병", "분할", "분할합병", "주식교환", "주식이전", "최대주주변경",
        "공개매수", "주식양수도",
        // 위험/존속
        "횡령", "배임", "부도", "당좌거래정지", "회생절차", "파산", "채권은행",
        "상장폐지", "관리종목", "실질심사", "영업정지", "생산중단", "거래정지",
        // 주식 단위
        "액면분할", "주식분할", "주식병합",
        // 채무
        "채무보증", "채무인수",
    )

    /** 보유자가 굳이 알 필요 없는 루틴·신고성 공시 → 푸시 제외, 앱에서 기본 숨김. */
    private val LOW = listOf(
        "기업설명회", "IR개최", "기업설명회(IR)",
        "대량보유상황보고", "특정증권등소유상황", "임원ㆍ주요주주", "임원주요주주",
        "주식등의대량보유", "최대주주등소유주식변동",
        "의결권대리행사", "위임장용지", "참고서류",
        "사업보고서", "반기보고서", "분기보고서", "감사보고서", "연결재무제표",
        "주주총회소집", "주주총회결과", "주주총회", "결산실적공시예고", "공시예고",
        "안내공시", "명의개서", "주주명부", "증권발행실적", "증권신고서효력",
        "기업지배구조보고서", "기업설명회개최",
    )

    fun classify(reportNm: String): DisclosureImportance {
        val norm = normalize(reportNm)
        if (HIGH.any { norm.contains(it) }) return DisclosureImportance.HIGH
        if (LOW.any { norm.contains(it) }) return DisclosureImportance.LOW
        return DisclosureImportance.MEDIUM
    }

    /**
     * 부분일치 정확도를 위해 키워드를 끊는 구분자(공백·가운뎃점류·마침표·괄호류)를 모두 제거.
     * 괄호 "문자"만 지우고 내용은 보존한다 — 예: "영업(잠정)실적" → "영업잠정실적"(→ "잠정실적" 매칭),
     * "[기재정정]단일판매" → "기재정정단일판매"(→ "단일판매" 매칭).
     */
    private fun normalize(s: String): String =
        s.replace(Regex("[\\s·ㆍ‧⋅・.()\\[\\]（）「」『』]"), "")
}
